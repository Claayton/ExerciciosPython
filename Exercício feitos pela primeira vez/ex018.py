import math
angulo = float(input('Digite o valor de um ângulo: '))
print('O ceno desse ângulo é: {:.2f}'.format(math.sin(math.radians(angulo))))
print('O cosseno desse ângulo é: {:.2f}'.format(math.cos(math.radians(angulo))))
print('A tangente desse ângulo é:{:.2f}'.format(math.tan(math.radians(angulo))))
print('xD')
